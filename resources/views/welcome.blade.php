<script>
var iniMarca   = '';	
var iniModelo  = '';	

function loadMarcas() {
	$.ajax({
		url:"http://escobarusados.cerbero.ultrait.com.ar/usados/fetch_marcas",
		method:"POST",
		dataType: 'json',
		success:function(response) {
			$('#marca').empty();
			$('#marca').append('<option value=""></option>');
			$.each(response,function(index,data){
				$('#marca').append('<option value="'+data['id']+'">'+data['marca']+'</option>');
			});
			$('#marca').val(iniMarca);
			iniMarca='';
			loadModelos();
		}
	});
}

function loadModelos() {
	var marcaId = $("#marca").val();
	$.ajax({
		url:"http://escobarusados.cerbero.ultrait.com.ar/usados/fetch_modelos_publicados",
		method:"POST",
		data:{marcaId:marcaId},
		dataType: 'json',
		success:function(response) {
			$('#modelo').empty();
			$('#modelo').append('<option value=""></option>');
			$.each(response,function(index,data){
				$('#modelo').append('<option value="'+data['id']+'">'+data['modelo']+'</option>');
			});
			$('#modelo').val(iniModelo);
			iniModelo='';
		}
	});
}

window.onload = function(e) { 
	var form = $( "#form1" );
	form.validate({
		rules: {
			marca: {},
			modelo: {},
			maxPrice: {number: true, min: 1000}
		}
	});

	$("#marca").change(loadModelos);
	loadMarcas();
}
